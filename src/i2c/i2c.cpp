// Draws text into the page-format framebuffer at the given page row (0-7) and column (0-127)
static void draw_string(uint8_t *buf, int col, int page, const char *str) {
    for (; *str && col < OLED_W; str++, col += 6) {
        uint8_t ch = (uint8_t)*str;
        if (ch < 0x20 || ch > 0x7E) ch = 0x20;
        const uint8_t *g = font5x8[ch - 0x20];
        for (int c = 0; c < 5 && col + c < OLED_W; c++) {
            buf[page * OLED_W + col + c] = g[c];
        }
        // col+5 stays 0 (spacing); buf is zeroed before use
    }
}

I2CMaster::~I2CMaster() {
    if (dev_handle) i2c_master_bus_rm_device(dev_handle);
    if (mp6050_handle) i2c_master_bus_rm_device(mp6050_handle);
    if (bus_handle) i2c_del_master_bus(bus_handle);
}

bool I2CMaster::init() {
    i2c_master_bus_config_t bus_cfg = {
        .i2c_port = I2C_NUM_0,
        .sda_io_num = I2C_MASTER_SDA_PIN,
        .scl_io_num = I2C_MASTER_SCL_PIN,
        .clk_source = I2C_CLK_SRC_DEFAULT,
        .glitch_ignore_cnt = 7,
        .intr_priority = 0,
        .trans_queue_depth = 0,
        .flags = {
            .enable_internal_pullup = true,
            .allow_pd = false,
        },
    };

    esp_err_t ret = i2c_new_master_bus(&bus_cfg, &bus_handle);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Failed to init I2C bus: %s", esp_err_to_name(ret));
        return false;
    }

    i2c_device_config_t dev_cfg = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address = SLAVE_ESP32,
        .scl_speed_hz = I2C_MASTER_FREQ_HZ,
        .scl_wait_us = 1000,
        .flags = { .disable_ack_check = false },
    };
    ret = i2c_master_bus_add_device(bus_handle, &dev_cfg, &dev_handle);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Failed to add slave ESP32: %s", esp_err_to_name(ret));
        return false;
    }
    ESP_LOGI(TAG, "ESP32 slave added");

    i2c_device_config_t mpu_cfg = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address = SLAVE_MP6050,
        .scl_speed_hz = I2C_MASTER_FREQ_HZ,
        .scl_wait_us = 1000,
        .flags = { .disable_ack_check = false },
    };
    ret = i2c_master_bus_add_device(bus_handle, &mpu_cfg, &mp6050_handle);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Failed to add MPU-6050: %s", esp_err_to_name(ret));
        return false;
    }
    ESP_LOGI(TAG, "MPU-6050 added");

    return true;
}

void I2CMaster::scan() {
    ESP_LOGI(TAG, "Scanning I2C bus...");
    for (uint8_t addr = 1; addr < 127; addr++) {
        esp_err_t ret = i2c_master_probe(bus_handle, addr, I2C_MASTER_TIMEOUT_MS / 2);
        if (ret == ESP_OK) {
            ESP_LOGI(TAG, "Device found at address 0x%02X", addr);
        }
    }
    ESP_LOGI(TAG, "I2C scan completed");
}

int I2CMaster::read_humidity() {
    uint8_t write_buf[1] = {CMD_GET_HUMIDITY};
    uint8_t read_buf[1] = {0};

    esp_err_t ret = i2c_master_transmit(dev_handle, write_buf, sizeof(write_buf), I2C_MASTER_TIMEOUT_MS);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Failed to send humidity command: %s", esp_err_to_name(ret));
        return -1;
    }
    vTaskDelay(pdMS_TO_TICKS(20));
    ret = i2c_master_receive(dev_handle, read_buf, sizeof(read_buf), I2C_MASTER_TIMEOUT_MS);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Failed to receive humidity: %s", esp_err_to_name(ret));
        return -1;
    }
    return read_buf[0];
}

float I2CMaster::read_temperature() {
    uint8_t write_buf[1] = {CMD_GET_TEMPERATURE};
    uint8_t read_buf[4];

    esp_err_t ret = i2c_master_transmit(dev_handle, write_buf, sizeof(write_buf), I2C_MASTER_TIMEOUT_MS);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Failed to send temperature command: %s", esp_err_to_name(ret));
        return -999.0f;
    }
    vTaskDelay(pdMS_TO_TICKS(20));
    ret = i2c_master_receive(dev_handle, read_buf, sizeof(read_buf), I2C_MASTER_TIMEOUT_MS);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Failed to receive temperature: %s", esp_err_to_name(ret));
        return -999.0f;
    }
    float temperature;
    std::memcpy(&temperature, read_buf, sizeof(float));
    return temperature;
}

void i2c_task(void *param) {
    I2CMaster *master = static_cast<I2CMaster *>(param);
    master->scan();

    // Init OLED via esp_lcd (SSD1306 driver is built into IDF 6)
    esp_lcd_panel_io_handle_t io = nullptr;
    esp_lcd_panel_io_i2c_config_t io_cfg = {
        .dev_addr = SLAVE_OLED,
        .scl_speed_hz = I2C_MASTER_FREQ_HZ,
        .control_phase_bytes = 1,
        .dc_bit_offset = 6,
        .lcd_cmd_bits = 8,
        .lcd_param_bits = 8,
        .on_color_trans_done = nullptr,
        .user_ctx = nullptr,
        .flags = {},
    };
    ESP_ERROR_CHECK(esp_lcd_new_panel_io_i2c(master->get_bus_handle(), &io_cfg, &io));

    esp_lcd_panel_handle_t panel = nullptr;
    esp_lcd_panel_dev_config_t panel_cfg = {};
    panel_cfg.reset_gpio_num = GPIO_NUM_NC;
    panel_cfg.bits_per_pixel = 1;
    ESP_ERROR_CHECK(esp_lcd_new_panel_ssd1306(io, &panel_cfg, &panel));
    ESP_ERROR_CHECK(esp_lcd_panel_reset(panel));
    ESP_ERROR_CHECK(esp_lcd_panel_init(panel));
    ESP_ERROR_CHECK(esp_lcd_panel_disp_on_off(panel, true));

    // Framebuffer in SSD1306 page format: buf[page * 128 + col]
    // Each byte = 8 vertical pixels (bit0 = top of the page row)
    static uint8_t buf[OLED_W * (OLED_H / 8)];
    memset(buf, 0, sizeof(buf));
    draw_string(buf, 0, 0, "Hello ESP32!");

    ESP_ERROR_CHECK(esp_lcd_panel_draw_bitmap(panel, 0, 0, OLED_W, OLED_H, buf));


    uint8_t reg = 0x75;
    uint8_t who_am_i = 0;
    esp_err_t ret = i2c_master_transmit_receive(master->get_mp6050_handle(), &reg, 1, &who_am_i, 1, I2C_MASTER_TIMEOUT_MS);

    if (ret != ESP_OK) {
        ESP_LOGE("MPU6050", "I2C error: %s", esp_err_to_name(ret));
    } else if (who_am_i != 0x68 && who_am_i != 0x70) {
        ESP_LOGW("MPU6050", "Unexpected WHO_AM_I = 0x%02X", who_am_i);
    } else {
        ESP_LOGI("MPU6050", "Device found! WHO_AM_I = 0x%02X", who_am_i);
    }


    while (true) {
        float temperature = master->read_temperature();
        int humidity = master->read_humidity();

        memset(buf, 0, sizeof(buf));
        draw_string(buf, 0, 0, "Hello ESP32!");

        if (temperature != -999.0f && humidity != -1) {
            char line1[32], line2[32];
            snprintf(line1, sizeof(line1), "Temp: %.1f C", temperature);
            snprintf(line2, sizeof(line2), "Hum:  %d%%", humidity);
            draw_string(buf, 0, 1, line1);
            draw_string(buf, 0, 2, line2);
            ESP_LOGI(TAG, "Temp: %.2f C, Humidity: %d%%", temperature, humidity);
        } else {
            draw_string(buf, 0, 1, "Sensor error");
            ESP_LOGW(TAG, "Communication error");
        }

        esp_lcd_panel_draw_bitmap(panel, 0, 0, OLED_W, OLED_H, buf);
        vTaskDelay(pdMS_TO_TICKS(1000));
    }
}
